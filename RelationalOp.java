public class RelationalOp
{
    public static void main(String[] args){
        int a=2,b=4;
        System.out.println("a = " + a);
        System.out.println("b= " +b);
        System.out.println("a<b = "+(a>b));
        System.out.println("a>b = "+(a>b));
        System.out.println("a<=b "+(a<=b));
        System.out.println("a>=b "+(a>=b));
        System.out.println("a==b "+(a==b));
        System.out.println("a!=b "+(a!=b));
    }
    
}
