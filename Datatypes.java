public class Datatypes {
    public static void main(String arg[])
    {
        float a=10f,b=2.5f;
        System.out.println(a);
        System.out.println(b);
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        
    }
    
}
